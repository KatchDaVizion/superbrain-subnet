def _generate_test_keys() -> tuple[Ed25519PrivateKey, str]:
    """Generate a fresh Ed25519 key pair and derive a node_id.

    Returns:
        (private_key, node_id) where node_id is the SHA-256 hex digest of the
        public key bytes (raw, 32-byte encoding).
    """
    private_key = Ed25519PrivateKey.generate()
    pub_bytes = private_key.public_key().public_bytes(
        Encoding.Raw, PublicFormat.Raw
    )
    node_id = hashlib.sha256(pub_bytes).hexdigest()
    return private_key, node_id


async def _run_server() -> None:
    """Entry-point coroutine: create a server with test keys and run it."""
    private_key, node_id = _generate_test_keys()
    store = ChunkStore()

    server = LANSyncServer(
        host=DEFAULT_HOST,
        port=DEFAULT_PORT,
        node_id=node_id,
        private_key=private_key,
        chunk_store=store,
    )

    loop = asyncio.get_running_loop()

    async def _shutdown(sig_name: str) -> None:
        logger.info("Received %s — initiating shutdown", sig_name)
        await server.stop()

    for sig in (signal.SIGINT, signal.SIGTERM):
        loop.add_signal_handler(
            sig,
            lambda s=sig: asyncio.ensure_future(_shutdown(s.name)),
        )

    logger.info("Node ID: %s", node_id)
    await server.start()


if __name__ == "__main__":
    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s [%(levelname)s] %(name)s: %(message)s",
        datefmt="%Y-%m-%dT%H:%M:%S",
    )
    asyncio.run(_run_server())

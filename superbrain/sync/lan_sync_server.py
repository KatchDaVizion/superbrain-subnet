# Constants
# ---------------------------------------------------------------------------

DEFAULT_HOST: str = "0.0.0.0"
DEFAULT_PORT: int = 8765
PROTOCOL_VERSION: str = "1.0"
PING_INTERVAL_S: int = 30
PING_TIMEOUT_S: int = 10

QDRANT_URL: str = "http://localhost:6333"
QDRANT_COLLECTION: str = "sb_docs_v1_ollama"
OLLAMA_URL: str = "http://localhost:11434"
EMBEDDING_MODEL: str = "nomic-embed-text"

MDNS_SERVICE_TYPE: str = "_superbrain._tcp.local."

VALID_MESSAGE_TYPES: Set[str] = {"handshake", "sync_request", "sync_response", "error"}

logger = logging.getLogger("superbrain.lan_sync")


# ---------------------------------------------------------------------------
# Data models
# ---------------------------------------------------------------------------


@dataclass
class Chunk:
    """A single knowledge chunk with its metadata and cryptographic proof."""

    hash: str
    content: str
    contributor_id: str
    timestamp: int
    visibility: str
    signature: str
    layer: str = "lan"

    def to_dict(self) -> Dict[str, Any]:
        """Serialize the chunk to a JSON-safe dictionary."""
        return {
            "hash": self.hash,
            "content": self.content,
            "contributor_id": self.contributor_id,
            "timestamp": self.timestamp,
            "visibility": self.visibility,
            "signature": self.signature,
            "layer": self.layer,
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> Chunk:
        """Deserialize a chunk from a dictionary.

        Raises:
            KeyError: If required fields are missing.
            TypeError: If field types are wrong.
        """
        return cls(
            hash=str(data["hash"]),
            content=str(data["content"]),
            contributor_id=str(data["contributor_id"]),
            timestamp=int(data["timestamp"]),
            visibility=str(data["visibility"]),
            signature=str(data["signature"]),
            layer=str(data.get("layer", "lan")),
        )


@dataclass
class PeerInfo:
    """Metadata about a connected peer."""

    node_id: str
    version: str
    chunk_count: int
    connected_at: float = field(default_factory=time.time)
    last_seen: float = field(default_factory=time.time)


# ---------------------------------------------------------------------------
# ChunkStore — thread-safe in-memory chunk storage
# ---------------------------------------------------------------------------


class ChunkStore:
    """Thread-safe in-memory store for knowledge chunks.

    Provides O(1) lookup by hash and safe concurrent access from both the
    asyncio event loop and any background threads (e.g. mDNS callbacks).
    """

    def __init__(self) -> None:
        self._lock: threading.Lock = threading.Lock()
        self._chunks: Dict[str, Chunk] = {}

    def has(self, chunk_hash: str) -> bool:
        """Check whether a chunk with the given hash exists."""
        with self._lock:
            return chunk_hash in self._chunks

    def get(self, chunk_hash: str) -> Optional[Chunk]:
        """Return a chunk by hash, or ``None`` if not found."""
        with self._lock:
            return self._chunks.get(chunk_hash)

    def put(self, chunk: Chunk) -> bool:
        """Store a chunk. Returns ``True`` if it was new, ``False`` if duplicate."""
        with self._lock:
            if chunk.hash in self._chunks:
                return False
            self._chunks[chunk.hash] = chunk
            return True

    def hashes(self) -> List[str]:
        """Return a snapshot of all stored chunk hashes."""
        with self._lock:
            return list(self._chunks.keys())

    def count(self) -> int:
        """Return the number of stored chunks."""
        with self._lock:
            return len(self._chunks)

    def get_delta(self, remote_manifest: Set[str]) -> List[Chunk]:
        """Return chunks that are in this store but NOT in *remote_manifest*."""
        with self._lock:
            local_hashes = set(self._chunks.keys())
            missing = local_hashes - remote_manifest
            return [self._chunks[h] for h in missing]



"""
SuperBrain LAN Sync Server

Production-quality asyncio WebSocket server for peer-to-peer knowledge chunk
synchronization over LAN. Part of the SuperBrain decentralized AI knowledge
network on Bittensor.

Protocol overview:
    1. Peers discover each other via mDNS (_superbrain._tcp.local.)
    2. Connecting peer sends HANDSHAKE with node_id and chunk_count
    3. Either side may send SYNC_REQUEST with a manifest of known chunk hashes
    4. Responder computes delta and replies with SYNC_RESPONSE containing only
       chunks the requester does not already have
    5. Received chunks are verified (Ed25519 signature), then ingested into
       Qdrant with embeddings generated by Ollama nomic-embed-text
"""

